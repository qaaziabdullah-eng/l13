import turtle
turtle.Screen().bgcolor("purple")
turtle.Screen().setup(500, 500)
turtle.Screen().title("Turtle")
t= turtle.Turtle()
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)

t.penup()
t.right(150)
t.forward(100)

t.pendown()
t.right(90)
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
turtle.done()